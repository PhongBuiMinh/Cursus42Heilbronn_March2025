#include "get_next_line.h"

char	*ft_strdup(const char *s1, int len)
{
	char	*dst;
	size_t	i;

	i = 0;
	dst = malloc(len + 1);
	if (dst == NULL || s1 == NULL)
		return (NULL);
	while (s1[i])
	{
		dst[i] = s1[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
